package cn.gov.cnis.cssn.wssort;

import java.io.Serializable;
/**
 * 提供标准信息列表类
 *
 */
public class StandardList implements Serializable {
	private String a001; // 记录标识符
	private String a100; // 文献号
	private String a000; // 记录状态
	private String A101;// 发布或出版日期
	private String a205;// 实施或试行日期
	private String a298;// 中文标准名称
	private String a302;// 英文标准名称
	private String a104;//标准品种
	private String a104_CN;//标准品种名称
	private String hasElec;// 是否有电子文档(是:1)
	private String price;//价格
	
	
	/**
	 * 获取记录识别符
	 * @return 记录识别符
	 */
	public String getA001() {
		return a001;
	}
	/**
	 * 设置记录识别符
	 * @param a001 记录识别符
	 */
	public void setA001(String a001) {
		this.a001 = a001;
	}
	/**
	 * 获取标准号
	 * @return  标准号
	 */
	public String getA100() {
		return a100;
	}
	/**
	 * 设置标准号
	 * @param a100 标准号
	 */
	public void setA100(String a100) {
		this.a100 = a100;
	}
	/**
	 * 获取记录状态
	 * @return 记录状态
	 */
	public String getA000() {
		return a000;
	}
	/**
	 * 设置记录状态
	 * @param a000 记录状态
	 */
	public void setA000(String a000) {
		this.a000 = a000;
	}
	/**
	 * 获取发布日期
	 * @return 发布日期
	 */
	public String getA101() {
		return A101;
	}
	/**
	 * 设置发布日期
	 * @param a101 发布日期
	 */
	public void setA101(String a101) {
		A101 = a101;
	}
	/**
	 * 获取实施日期
	 * @return 实施日期
	 */
	public String getA205() {
		return a205;
	}
	/**
	 * 设置实施日期
	 * @param a205 实施日期
	 */
	public void setA205(String a205) {
		this.a205 = a205;
	}
	/**
	 * 获取中文标准名称
	 * @return 中文标准名称
	 */
	public String getA298() {
		return a298;
	}
	/**
	 * 设置中文标准名称
	 * @param a298 中文标准名称
	 */
	public void setA298(String a298) {
		this.a298 = a298;
	}
	/**
	 * 获取英文标准名称
	 * @return 英文标准名称
	 */
	public String getA302() {
		return a302;
	}
	/**
	 * 设置英文标准名称
	 * @param a302 英文标准名称
	 */
	public void setA302(String a302) {
		this.a302 = a302;
	}
	
	/**
	 * 获取标准品种
	 * @return 发布单位
	 */
	public String getA104() {
		return a104;
	}
	/**
	 * 设置标准品种
	 * @param a104 标准品种
	 */
	public void setA104(String a104) {
		this.a104 = a104;
	}
	/**
	 * 获取标准品种名称
	 * @return 标准品种名称
	 */
	public String getA104_CN() {
		return a104_CN;
	}
	/**
	 * 设置标准品种名称
	 * @param a104CN 标准品种名称
	 */
	public void setA104_CN(String a104CN) {
		a104_CN = a104CN;
	}
	
	/**
	 * 获取是否有电子文档
	 * @return 是否有电子文档
	 */
	public String getHasElec() {
		return hasElec;
	}
	/**
	 * 设置是否有电子文档
	 * @param hasElec 是否有电子文档
	 */
	public void setHasElec(String hasElec) {
		this.hasElec = hasElec;
	}
	/**
	 * 获取价格
	 * @return 价格
	 */
	public String getPrice() {
		return price;
	}
	/**
	 * 设置价格
	 * @param price 价格
	 */
	public void setPrice(String price) {
		this.price = price;
	}
	
}
